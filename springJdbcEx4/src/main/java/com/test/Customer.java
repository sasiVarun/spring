package com.test;

public class Customer {

	private String customerId;
	private String customerName;
	private String customerAddress;
	private String customerLoginDate;
	private String customerLoginDateTime;

	public String getCustomerId() {
		return customerId;
	}

	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getCustomerAddress() {
		return customerAddress;
	}

	public void setCustomerAddress(String customerAddress) {
		this.customerAddress = customerAddress;
	}

	public String getCustomerLoginDate() {
		return customerLoginDate;
	}

	public void setCustomerLoginDate(String customerLoginDate) {
		this.customerLoginDate = customerLoginDate;
	}

	public String getCustomerLoginDateTime() {
		return customerLoginDateTime;
	}

	public void setCustomerLoginDateTime(String customerLoginDateTime) {
		this.customerLoginDateTime = customerLoginDateTime;
	}

}
